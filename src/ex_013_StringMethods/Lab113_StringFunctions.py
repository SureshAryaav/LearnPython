#Access characters using their index (starting from 0).
msg = "Python"
print(msg[0])  # Output: P
print(msg[-1])  # Output: n (last character)

# Slicing - Extract parts of a string.
msg = "Hello, Python!"
print(msg[0:5])   # Output: Hello
print(msg[7:])    # Output: Python!
print(msg[:5])    # Output: Hello
print(msg[-7:])   # Output: Python!
