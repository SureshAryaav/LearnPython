# Strings
name = "Alice"
print(name)

# Integers
age = 25
print(age)

# Float
height = 5.6
print(height)

# Boolean
is_student = True
print(is_student)

print("Name:", name)
print("Age:", age)
print("Is student?: ", is_student)

print(type(name))       # <class 'str'>
print(type(age))        # <class 'int'>